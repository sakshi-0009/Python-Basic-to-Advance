''' Numeric data type have three types : 1.int, 2.float, 3.complexData'''

#   1.int

empId = 101
print(empId)
print(type(empId))  #<class 'int'>

#   2.float

empSalary = 450000.50
print(empSalary)
print(type(empSalary))  #<class 'float'>

#   3.complex

complexData = 10+7j
print(complexData)
print(type(complexData))    #<class 'complex'>